async def write_file(
    path: str,
    content: str,
    overwrite: bool = True,
    tool_helper: ToolHelper = None,
) -> str:
    """
    Create or update a text file in the authenticated user's workspace.
    When writing Python files, use print() for any output you need to see — stdout is the only channel back to you after execution.
    Scripts run in an isolated temp directory, so any files created during execution are discarded. Do not rely on files generated by executed scripts; only print() output is accessible.

    Args:
        path: Absolute workspace file path to write (e.g. "/src/app.py").
        content: UTF-8 text content to store in the file.
        overwrite: When false, fail if the target file already exists.
    """
    try:
        if tool_helper is None:
            return json.dumps({"error": "Missing tool helper dependency"})

        user_id, workspace_service = tool_helper.require_workspace_context()

        if not overwrite:
            try:
                await workspace_service.read_file(user_id=user_id, path=path)
                return json.dumps({"error": f"File already exists: {path}"})
            except FileNotFoundError:
                pass

        result = await workspace_service.write_text_file(
            user_id=user_id,
            path=path,
            content=content,
        )
        return json.dumps(result.model_dump(mode="json"), default=str)
    except Exception as exc:
        return json.dumps({"error": str(exc) or "Failed to write file"})


async def edit_file(
    path: str,
    old_text: str,
    new_text: str,
    replace_all: bool = False,
    tool_helper: ToolHelper = None,
) -> str:
    """
    Edit an existing text file by replacing text content.

    Args:
        path: Absolute workspace file path to edit (e.g. "/src/app.py").
        old_text: The exact text to find in the file.
        new_text: The replacement text.
        replace_all: When true, replace every occurrence. When false, replace only the first occurrence.
    """
    try:
        if tool_helper is None:
            return json.dumps({"error": "Missing tool helper dependency"})
        if not isinstance(old_text, str) or old_text == "":
            return json.dumps({"error": "old_text must be a non-empty string"})

        user_id, workspace_service = tool_helper.require_workspace_context()
        file_entry = await workspace_service.read_file(user_id=user_id, path=path)

        content = file_entry.content
        if not isinstance(content, str):
            return json.dumps({"error": f"Workspace file is not valid UTF-8 text: {path}"})

        total_matches = content.count(old_text)
        if total_matches == 0:
            return json.dumps({"error": f"Text not found in file: {path}"})

        updated_content = content.replace(old_text, new_text) if replace_all else content.replace(old_text, new_text, 1)
        replacements = total_matches if replace_all else 1

        if updated_content == content:
            return json.dumps(
                {
                    "path": file_entry.path,
                    "edited": False,
                    "replacements": 0,
                    "message": "No changes were written because old_text and new_text are identical.",
                }
            )

        result = await workspace_service.write_text_file(
            user_id=user_id,
            path=file_entry.path,
            content=updated_content,
        )
        payload = result.model_dump(mode="json")
        payload["edited"] = True
        payload["replacements"] = replacements
        payload["replace_all"] = bool(replace_all)
        return json.dumps(payload, default=str)
    except Exception as exc:
        return json.dumps({"error": str(exc) or "Failed to edit file"})


