from __future__ import annotations

import json
from collections import deque
from typing import Any

from sequence.agent.helpers.tool_helper import ToolHelper
from sequence.models.workspace import WorkspaceNodeType

_MAX_FOLDER_FILES = 100
_MAX_FILE_CONTENT_CHARS = 20_000


async def read_file(
    path: str,
    tool_helper: ToolHelper = None,
) -> str:
    """
    Read a text file from the authenticated user's workspace.

    Args:
        path: Absolute workspace file path to read (e.g. "/notes/todo.md").
    """
    try:
        if tool_helper is None:
            return json.dumps({"error": "Missing tool helper dependency"})

        user_id, workspace_service = tool_helper.require_workspace_context()
        file_entry = await workspace_service.read_file(user_id=user_id, path=path)
        payload = file_entry.model_dump(mode="json")

        content = payload.get("content")
        if isinstance(content, str) and len(content) > _MAX_FILE_CONTENT_CHARS:
            payload["content"] = content[:_MAX_FILE_CONTENT_CHARS]
            payload["content_truncated"] = True
            payload["content_original_length"] = len(content)

        return json.dumps(payload, default=str)
    except Exception as exc:
        return json.dumps({"error": str(exc) or "Failed to read file"})


async def read_folder(
    path: str,
    recursive: bool = False,
    max_files: int = 50,
    tool_helper: ToolHelper = None,
) -> str:
    """
    Read files from a workspace folder.

    Args:
        path: Absolute workspace folder path to read (e.g. "/src").
        recursive: When true, include files from nested folders.
        max_files: Maximum number of files to return (clamped to 1-100).
    """
    try:
        if tool_helper is None:
            return json.dumps({"error": "Missing tool helper dependency"})

        user_id, workspace_service = tool_helper.require_workspace_context()
        capped_max_files = max(1, min(int(max_files), _MAX_FOLDER_FILES))

        queue: deque[str] = deque([path])
        files: list[dict[str, Any]] = []

        while queue and len(files) < capped_max_files:
            current_folder = queue.popleft()
            listing = await workspace_service.list_directory_children(
                user_id=user_id,
                path=current_folder,
            )

            for child in listing.children:
                if child.node_type == WorkspaceNodeType.FOLDER:
                    if recursive:
                        queue.append(child.path)
                    continue

                file_entry = await workspace_service.read_file(user_id=user_id, path=child.path)
                payload = file_entry.model_dump(mode="json")
                content = payload.get("content")
                if isinstance(content, str) and len(content) > _MAX_FILE_CONTENT_CHARS:
                    payload["content"] = content[:_MAX_FILE_CONTENT_CHARS]
                    payload["content_truncated"] = True
                    payload["content_original_length"] = len(content)

                files.append(payload)
                if len(files) >= capped_max_files:
                    break

        result = {
            "path": path,
            "recursive": recursive,
            "max_files": capped_max_files,
            "returned_files": len(files),
            "truncated": len(files) >= capped_max_files,
            "files": files,
        }
        return json.dumps(result, default=str)
    except Exception as exc:
        return json.dumps({"error": str(exc) or "Failed to read folder"})


